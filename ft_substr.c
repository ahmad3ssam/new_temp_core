#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*ptr;
	size_t	i;

	if (!s)
		return (NULL);
	ptr = malloc(len + 1);
	if (!ptr || start >= ft_strlen(s))
		return (ft_strdup(""));
	i = 0;
	while (i < len && s[start + i] != '\0')
	{
		ptr[i] = s[start + i];
		i++;
	}
	ptr[i + start] = '\0';
	return (ptr);
}
